function theTruth() {
  return true;
}

function sayHey() {
  return 'Hey!';
}

function sayHeyFriend(friend) {
  return "Hey," + " " + `${friend}` + "!";
}

function favoriteIceCream(flavor) {
  return `I love ${flavor}`;
}

function shouting(str) {
  return `${str.toUpperCase()}`;
}

function roundDown(num) {
  return Math.floor(num);
}