function theTruth() {
  return true;
}

function sayHey() {
  return "Hey!";
}

function sayHeyFriend(friendName) {
  return `Hey, ${friendName}!`;
}

function favoriteIceCream(nameOfIceCream) {
  return `I love ${nameOfIceCream}`;
}

function shouting(phrase) {
  return `${phrase.toUpperCase()}`;
}

function roundDown(number) {
  return `${Math.floor(number)}`;
}
